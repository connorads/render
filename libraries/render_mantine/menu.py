from render import create_callback, Component, InputComponent


class Menu(InputComponent):
    Module = "mantine"
    JSXName = "Menu"
    InputName = "opened"

    def __init__(
        self,
        children=None,
        key=None,
        style=None,
        className=None,
        id=None,
        onKeyPress=None,
        onClick=None,
        onChange=None,
        defaultOpened=None,
        opened=None,
        classNames=None,
        clickOutsideEvents=None,
        closeDelay=None,
        closeOnClickOutside=None,
        closeOnEscape=None,
        closeOnItemClick=None,
        loop=None,
        m=None,
        mb=None,
        ml=None,
        mr=None,
        mt=None,
        mx=None,
        my=None,
        onClose=None,
        onOpen=None,
        openDelay=None,
        p=None,
        pb=None,
        pl=None,
        pr=None,
        pt=None,
        px=None,
        py=None,
        styles=None,
        sx=None,
        trigger=None,
        unstyled=None,
        controller=None,
    ):
        super().__init__(key, controller, onChange, opened, defaultOpened)
        self.children = children
        self.style = style
        self.className = className
        self.id = id
        self.onKeyPress = create_callback(onKeyPress)
        self.onClick = create_callback(onClick)
        self.classNames = classNames
        self.clickOutsideEvents = clickOutsideEvents
        self.closeDelay = closeDelay
        self.closeOnClickOutside = closeOnClickOutside
        self.closeOnEscape = closeOnEscape
        self.closeOnItemClick = closeOnItemClick
        self.loop = loop
        self.m = m
        self.mb = mb
        self.ml = ml
        self.mr = mr
        self.mt = mt
        self.mx = mx
        self.my = my
        self.onClose = create_callback(onClose)
        self.onOpen = create_callback(onOpen)
        self.openDelay = openDelay
        self.p = p
        self.pb = pb
        self.pl = pl
        self.pr = pr
        self.pt = pt
        self.px = px
        self.py = py
        self.styles = styles
        self.sx = sx
        self.trigger = trigger
        self.unstyled = unstyled
        assert id is None or isinstance(id, str)


